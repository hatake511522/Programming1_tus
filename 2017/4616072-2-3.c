#include <stdio.h>

int main(void) {
  int a,n,y;
  int x[100000] ={};
  x[n+1] = a*x[n]^2+1;
  x[0] = y;
  printf("定数の値を入力してください\n");
  printf("aの値\n");
  scanf("%d\n", &a);
  printf("x[0]の値\n");
  scanf("%d\n", &y);
  while(n=0,n<100000){
    printf("%d\n", x[n]);
    n++;
  }
  return 0;
}
